public class Passageiro {
    private String nome;
    private String enderecoAtual, enderecoDestino;

    public void setNome(String nome) {this.nome = nome;}
    public void setEnderecoAtual(String enderecoAtual) {this.enderecoAtual = enderecoAtual;}
    public void setEnderecoDestino(String enderecoDestino) {this.enderecoDestino = enderecoDestino;}

    public String getNome() {return nome;}
    public String getEnderecoAtual() {return enderecoAtual;}
    public String getEnderecoDestino() {return enderecoDestino;}
}
